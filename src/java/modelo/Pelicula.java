package modelo;

/**
 *
 * @author usuario
 */
public class Pelicula {
    String nombre, descripcion, lengua, rating;
    int ano, duracion;

    public Pelicula(String nombre, String descripcion, String lengua, String rating, int ano, int duracion) {
        this.nombre = nombre;
        this.descripcion = descripcion;
        this.lengua = lengua;
        this.rating = rating;
        this.ano = ano;
        this.duracion = duracion;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getLengua() {
        return lengua;
    }

    public void setLengua(String lengua) {
        this.lengua = lengua;
    }

    public String getRating() {
        return rating;
    }

    public void setRating(String rating) {
        this.rating = rating;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public int getDuracion() {
        return duracion;
    }

    public void setDuracion(int duracion) {
        this.duracion = duracion;
    }
    
    
    
}
